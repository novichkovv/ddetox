        <h2>Roasted Butternut Soup</h2>
        <img class="recipe_image" src="<?php echo SITE_DIR; ?>images/recipes/butternut_soup.jpg" alt="Roasted Butternut Soup" width="300" height="226" align="left" />
        <h3>Ingredients</h3>
        <ul><li>1 (2 1/2-pound) butternut squash</li>
            <li>Cooking spray</li>
            <li>1 tablespoon extra-virgin olive oil</li>
            <li>1 1/2 cups chopped onion</li>
            <li>3 garlic cloves, minced</li>
            <li>6 cups Roasted Vegetable Stock</li>
            <li>2 cups coarsely chopped turnips</li>
            <li>2 teaspoons chopped fresh sage</li>
            <li>3/4 teaspoon salt</li>
            <li>1/4 teaspoon freshly ground black pepper</li>
            <li>1 bay leaf</li>
            <li>1 tablespoon finely chopped fresh chives</li>
            <li>2 tablespoons chopped fresh parsley</li>
            <li>2 teaspoons honey </li> </ul>
        <h3>Preparation</h3>
        <ol>
            <li>Preheat oven to 400°</li>
            <li>Cut squash in half lengthwise; discard seeds. Place squash, cut sides down, on a foil-lined baking sheet coated with cooking spray. Bake at 400° for 30 minutes or until tender. Cool. Discard peel; mash pulp.</li>
            <li>Preheat broiler.</li>
            <li>Heat a Dutch oven over medium-high heat. Add oil to pan; swirl to coat. Add onion; sauté 4 minutes, stirring occasionally. Add garlic; sauté 30 seconds, stirring constantly. Add squash, Roasted Vegetable Stock, and next 5 ingredients (through bay leaf); bring to a boil. Reduce heat, and simmer 45 minutes or until turnips are tender, stirring occasionally. Let stand 10 minutes. Discard bay leaf.</li>

            <li>Place one-third of vegetable mixture in a blender. Remove center piece of blender lid (to allow steam to escape); secure blender lid on blender. Place a clean towel over opening in blender lid (to avoid splatters). Blend until smooth. Pour into a large bowl. Repeat procedure twice with remaining squash mixture. Return pureed mixture to pan; cook over medium heat 3 minutes or until thoroughly heated. Stir in parsley and honey. Serve with toasts.</li>
        </ol>

