        <h2>Red Lentil-Pumpkin Soup </h2>
        <img class="recipe_image" src="<?php echo SITE_DIR; ?>images/recipes/red-lentil-pumpkin-soup.jpg" alt="Red Lentil-Pumpkin Soup " width="300" height="300" align="left" />
        <h3>Ingredients</h3>
        <ul><li>2 teaspoons coconut oil</li>
            <li>1 cup chopped onion</li>
            <li>1 teaspoon minced garlic</li>
            <li>3 1/2 cups organic vegetable broth, divided</li>
            <li>1 cup dried small red lentils</li>
            <li>1 teaspoon ground cumin</li>
            <li>1/4 teaspoon salt</li>
            <li>1/4 teaspoon ground cinnamon</li>
            <li>1 cup water</li>
            <li>3/4 cup fresh pumpkin</li>
            <li>1 tablespoon grated peeled fresh ginger</li>
            <li>1 tablespoon fresh lemon juice</li>
            <li>1/4 cup unsalted pumpkinseed kernels, toasted</li>
            <li>1/4 cup chopped fresh cilantro </li> </ul>
        <h3>Preparation</h3>
        <ol>
            <li>Heat a large Dutch oven over medium-high heat. </li>
            <li>Add oil to pan; swirl to coat. </li>
            <li>Add onion and garlic to pan; sauté 4 minutes. </li>
            <li>Stir in 3 cups broth, lentils, and next 4 ingredients (through red pepper); bring to a boil. Cover, reduce heat, and simmer 10 minutes or until lentils are tender. Place the lentil mixture in a blender. Remove center piece of blender lid (to allow steam to escape); secure blender lid on blender. Place a clean towel over opening in blender lid (to avoid splatters). Blend until smooth. Return lentil mixture to pan over medium heat. </li>
            <li>Add remaining 1/2 cup broth, 1 cup water, and pumpkin to pan; cook 3 minutes or until thoroughly heated. </li>
            <li>Stir in ginger and lemon juice. Ladle 1 1/2 cups soup into each of 4 bowls; top each serving with 1 tablespoon pumpkinseeds, and 1 tablespoon cilantro. </li>
        </ol>
