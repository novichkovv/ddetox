        <h2>Speedy Three-Bean Salad </h2>
        <img class="recipe_image"  src="<?php echo SITE_DIR; ?>images/recipes/speedy.jpeg" alt="Speedy Three-Bean Salad " width="300" height="299" align="left" />
        <h3>Ingredients</h3>
        <ul><li>1 cup chopped green beans, stems removed</li>
            <li>1 (15-oz) can chickpeas (I use Eden Organic BPA-free), or 1.5 cups cooked</li>
            <li>1 (15-oz) can Red Kidney Beans (ditto above), or 1.5 cups cooked</li>
            <li>1/4 summer squash finely chopped</li>
            <li>1/3 cup packed fresh parsley, finely chopped</li>
            <li>3 green onions, finely chopped</li>
            <li>Salt & pepper, to taste</li>
            <li>8 tbsp fresh lemon juice (about 1.5 lemons)</li>
            <li>1.5 tbsp extra virgin olive oil, or more to taste</li>
            <li>1 tbsp apple cider vinegar</li>
            <li>1 tbsp pure maple syrup</li>
            <li>1 tbsp Dijon mustard</li>
            <li>1/4 tsp fine grain sea salt  </li> </ul>
        <h3>Preparation</h3>
        <ol>
            <li>Add several cups of water into a medium-sized pot and bring to a boil. Add chopped green beans and blanch for 2-3 minutes in boiling water. Drain and rinse with cold water. Place into a large bowl.</li>


            <li>Drain and rinse chickpeas and kidney beans and place into a large bowl along with the green beans, sqash, parsley, and green onion. Mix.</li>


            <li>In a small bowl, whisk together the dressing ingredients until combined. Adjust to taste if needed.</li>


            <li>Pour on dressing over salad and mix well. Place in the fridge for 30 minutes to allow the flavours to develop. Season with salt and pepper and adjust dressing to taste. Salad will taste even better the next day. Store in the fridge for up to 3-4 days.
            </li>
        </ol>

