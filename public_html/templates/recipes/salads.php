        <h2>Dinosaur Dill Crunch Salad with Salt & Vinegar Chickpeas </h2>
        <img class="recipe_image"  src="<?php echo SITE_DIR; ?>images/recipes/dinosaur.jpg" alt="Dinosaur Dill Crunch Salad with Salt & Vinegar Chickpeas " width="300" height="201" align="left" />
        <h3>Ingredients</h3>
        <ul><li>1 (15-oz) can chickpeas, drained and rinsed</li>
            <li>2 cups white vinegar</li>
            <li>few pinches of kosher salt</li>
            <li>extra virgin olive oil, to lightly coat</li>
            <li>2 garlic cloves</li>
            <li>1/4 cup + 2 tbsp fresh lemon juice</li>
            <li>1/4 cup tahini</li>
            <li>2 tbsp water</li>
            <li>1 tbsp extra virgin olive oil</li>
            <li>1.5 tbsp nutritional yeast*</li>
            <li>1.5 cups packed fresh dill, stems removed and roughly chopped</li>
            <li>1/2-3/4 tsp kosher salt</li>
            <li>1-2 small bunches Dinosaur kale (also called Lacinato or Tuscan kale) OR 1 large bunch curly kale</li>
            <li>other greens if desired (I used a couple large, de-stemmed collard leaves)</li>
            <li>1 English cucumber, chopped small</li>
            <li>1/2 red onion, chopped</li>
            <li>1 batch of Lemon Dill Tahini dressing (below)</li>
            <li>few tablespoons of sesame seeds, toasted if preferred</li>
            <li>1/2 batch of Salt & Vinegar chickpeas </li> </ul>
        <h3>Preparation</h3>
        <ol>
            <li>Salt and vinegar chickpeas: Preheat oven to 425F and line baking sheet. In a pot, bring 2 cups white vinegar to a boil and add pinch of salt. Add in drained and rinsed chickpeas and bring to a boil for 1 minute (no longer). Some skins may fall off and that’s normal. Turn off heat and let the chickpeas sit in the vinegar for 30 minutes. Drain the chickpeas, place into pot, and light coat with a tiny bit of oil and a few pinches of kosher salt. Place on pan and roast for 35-40 minutes at 425F, until golden and crispy. Halfway through baking, give the pan a shake to rotate the peas. Keep an eye on them to avoid burning, especially near the end. Leave on pan until ready to eat. They are best served fresh as they get soft when stored.</li>
            <li> Dill dressing: In a mini chopper or food processor, mince the garlic. Now add in the rest of the ingredients, except for the dill, and process until smooth. Give the dill a rough chop and add into processor and process until smooth. Should keep in an air-tight container for 5-8 days in the fridge.            </li>
            <li>Salad: Prep all salad ingredients and add into a large bowl. Massage your desired amount of the dressing into the leaves and let it “marinate” until ready to serve. Add roasted chickpeas on top along with a drizzle of more dressing and sesame seeds. </li>
        </ol>

